// 引数が２つ出ない場合も快調
// １）引数チェック
// ２）重複チェック用の配列を用意。int seen[256]
// ３）１つ目の文字列の文字を１文字ずつチェック。
// 　　s1[i]がs2に含まれているか確認し、まだ出力していない文字なら出力してseenに記録する。

// ＊seen[256]→文字の出現記録で、seen['a'] = 1のように文字をインデックスとして使える。
