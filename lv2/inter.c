#include <unistd.h>

int	main(int argc, char **argv)
{
	int				i;
	int				j;
	char			seen[256];
	char			printed[256];
	unsigned char	c;

	i = 0;
	j = 0;
	while (i < 256)
	{
		seen[i] = 0;
		printed[i] = 0;
		i++;
	}
	i = 0;
	if (argc != 3)
	{
		write (1, "\n", 1);
		return (0);
	}
	while(argv[2][j])
	{
		c = argv[2][j];
		if(seen[c] == 0)
			seen[c] = 1;
		j++;
	}
	while (argv[1][i])
	{
		c = argv[1][i];
		if(seen[c] == 1 && c != ' ' && c != '\t' && printed[c] != 1)
		{
			write(1, &c, 1);
			printed[c] = 1;
		}
		if (c == ' ' || c == '\t')
			write(1, " ", 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}


