#include <stdio.h>

int		max(int* tab, unsigned int len)
{
	int	i;
	int	max;

	i = 0;
	if (!tab || len == 0 )
		return (0);
	max = tab[0];
	while (i < len)
	{
		if (max < tab[i])
			max = tab[i];
		i++;
	}
	return (max);
}

int	main(void)
{
	int	arr[] = {1, 5, -22, 6, 2};

	printf("%d\n", max(arr, 5));
	return (0);
}

